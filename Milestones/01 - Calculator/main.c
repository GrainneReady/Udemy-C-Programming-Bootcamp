#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("Addition: %d + %d = %d\n", 5, 2, 5+2);
    printf("Subtraction: %d - %d = %d\n", 5, 2, 5-2);
    printf("Multiplication: %d * %d = %d\n", 5, 2, 5*2);
    printf("Division: %d / %d = %d\n", 5, 2, 5/2); 
    printf("Remainder: %d %% %d = %d\n", 5, 2, 5%2);
    return 0;
}